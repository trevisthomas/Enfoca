import Foundation
import UIKit

func segues(ofViewController viewController: UIViewController) -> [String] {
    let identifiers = (viewController.value(forKey: "storyboardSegueTemplates") as? [AnyObject])?.flatMap({ $0.value(forKey: "identifier") as? String }) ?? []
    return identifiers
}
